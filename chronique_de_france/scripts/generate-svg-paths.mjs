import { readFileSync, writeFileSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");

// SVG viewport
const WIDTH = 600;
const HEIGHT = 680;

// France mercator projection parameters
const LON_CENTER = 2.5;
const LAT_CENTER = 46.5;
// Both lon and lat are converted to radians → same unit → correct aspect ratio
// SCALE=2300 fills the 600×680 viewBox for metropolitan France
const SCALE = 2300;
const LON_CENTER_RAD = (LON_CENTER * Math.PI) / 180;

function mercatorY(lat) {
  const rad = (lat * Math.PI) / 180;
  return Math.log(Math.tan(Math.PI / 4 + rad / 2));
}

const mercatorYCenter = mercatorY(LAT_CENTER);

function project([lon, lat]) {
  const lonRad = (lon * Math.PI) / 180;
  const x = (lonRad - LON_CENTER_RAD) * SCALE + WIDTH / 2;
  const y = HEIGHT / 2 - (mercatorY(lat) - mercatorYCenter) * SCALE;
  return [x, y];
}

function ringToPathCommands(ring) {
  if (ring.length === 0) return "";
  const [x0, y0] = project(ring[0]);
  const parts = [`M ${x0.toFixed(2)} ${y0.toFixed(2)}`];
  for (let i = 1; i < ring.length; i++) {
    const [x, y] = project(ring[i]);
    parts.push(`L ${x.toFixed(2)} ${y.toFixed(2)}`);
  }
  parts.push("Z");
  return parts.join(" ");
}

function geometryToPath(geometry) {
  if (geometry.type === "Polygon") {
    return geometry.coordinates.map(ringToPathCommands).join(" ");
  }
  if (geometry.type === "MultiPolygon") {
    return geometry.coordinates
      .flatMap((polygon) => polygon.map(ringToPathCommands))
      .join(" ");
  }
  return "";
}

const raw = readFileSync(join(ROOT, "public", "france-regions.geojson"), "utf8");
const geojson = JSON.parse(raw);

const paths = geojson.features.map((feature) => ({
  code: feature.properties.code,
  nom: feature.properties.nom,
  d: geometryToPath(feature.geometry),
}));

const output = `// Auto-generated by scripts/generate-svg-paths.mjs — do not edit manually

export type RegionPath = {
  code: string;
  nom: string;
  d: string;
};

export const FRANCE_SVG_PATHS: RegionPath[] = ${JSON.stringify(paths, null, 2)};
`;

writeFileSync(join(ROOT, "data", "france-svg-paths.ts"), output, "utf8");
console.log(`Generated ${paths.length} region paths → data/france-svg-paths.ts`);
